#!/bin/bash
#SBATCH --job-name=sst_vad_grid
#SBATCH --partition=batch
#SBATCH --gpus=1
#SBATCH --cpus-per-task=4
#SBATCH --mem=32G
#SBATCH --output=/data/jagucam1@alumno.upv.es/MAIN/experiments/baselines_trades/vad/slurm-%j.out

set -eo pipefail

# ── Environment setup ─────────────────────────────────────────────────────────
REPO_ROOT="/data/jagucam1@alumno.upv.es/MAIN"
CONDA_ENV="/data/jagucam1@alumno.upv.es/MAIN/env"
CONDA_ACTIVATE="/data/jagucam1@alumno.upv.es/miniconda3/bin/activate"

EXP_DIR="${REPO_ROOT}/experiments/baselines_trades/vad"
CONFIG_DIR="${EXP_DIR}/configs"
INFERENCES_DIR="${EXP_DIR}/inferences"

# ── 2D sweep: max_unvoiced_length x voice_threshold ──────────────────────────
PAUSE_VALUES=("0.1" "0.3" "0.5" "0.7" "1.0")
THRESHOLD_VALUES=("0.1" "0.2" "0.3" "0.5" "0.75")

# ── Fixed parameters ──────────────────────────────────────────────────────────
STEP_LENGTH="1.0"
MIN_SEGMENT_LENGTH="2.0"
MAX_SEGMENT_LENGTH="20.0"
SEAMLESS_MODEL="facebook/seamless-m4t-v2-large"
TGT_LANG="deu"

# ── Setup ─────────────────────────────────────────────────────────────────────
mkdir -p "${CONFIG_DIR}"
mkdir -p "${INFERENCES_DIR}"

cd "${REPO_ROOT}"
source "${CONDA_ACTIVATE}" "${CONDA_ENV}"

export PYTHONPATH="${REPO_ROOT}/src${PYTHONPATH:+:${PYTHONPATH}}"
export PATH="$HOME/.local/bin:$PATH"

# ── 2D parameter sweep ────────────────────────────────────────────────────────
for pause_val in "${PAUSE_VALUES[@]}"; do
  for threshold_val in "${THRESHOLD_VALUES[@]}"; do

    config_file="${CONFIG_DIR}/vad_pause${pause_val}_thr${threshold_val}.yaml"
    metrics_file="${INFERENCES_DIR}/vad_pause${pause_val}_thr${threshold_val}.jsonl"
    segstats_file="${INFERENCES_DIR}/vad_pause${pause_val}_thr${threshold_val}.segstats.json"

    # Auto-generate YAML config
    cat > "${config_file}" <<CONFIG
# Auto-generated by inferences_vad.sh
# pause=${pause_val}s | threshold=${threshold_val} | step=${STEP_LENGTH}s
type: "simulstream.server.speech_processors.simuleval_wrapper.SimulEvalWrapper"
simuleval_agent: "vad_segmenter.baseline_vad.SeamlessM4TAgentWithVADSegmenter"

latency_unit: "word"
speech_chunk_size: 0.5
detokenizer_type: "simuleval"

config:
  device: "cuda"
  step_length: ${STEP_LENGTH}
  max_unvoiced_length: ${pause_val}
  voice_threshold: ${threshold_val}
  min_segment_length: ${MIN_SEGMENT_LENGTH}
  max_segment_length: ${MAX_SEGMENT_LENGTH}
  seamless_m4t_model: "${SEAMLESS_MODEL}"
  seamless_m4t_tgt_lang: "${TGT_LANG}"
  segment_stats_file: "${segstats_file}"

agent_args:
  - "--step-length"
  - "${STEP_LENGTH}"
  - "--max-unvoiced-length"
  - "${pause_val}"
  - "--voice-threshold"
  - "${threshold_val}"
  - "--min-segment-length"
  - "${MIN_SEGMENT_LENGTH}"
  - "--max-segment-length"
  - "${MAX_SEGMENT_LENGTH}"
  - "--seamless-m4t-model"
  - "${SEAMLESS_MODEL}"
  - "--seamless-m4t-tgt-lang"
  - "${TGT_LANG}"
  - "--device"
  - "cuda"
  - "--segment-stats-file"
  - "${segstats_file}"
CONFIG

    # Skip if output already exists
    if [[ -f "${metrics_file}" ]]; then
      echo "[vad] $(basename ${metrics_file}) already exists, skipping..."
      continue
    fi

    echo "════════════════════════════════════════════════════════════"
    echo "[vad] pause=${pause_val}s | threshold=${threshold_val} | step=${STEP_LENGTH}s"
    echo "════════════════════════════════════════════════════════════"

    simulstream_inference \
      --speech-processor-config "${config_file}" \
      --wav-list-file "${REPO_ROOT}/data/dev/src.txt" \
      --tgt-lang deu \
      --src-lang eng \
      --metrics-log-file "${metrics_file}"

    echo "[vad] Done pause=${pause_val}s | threshold=${threshold_val}"
    echo "────────────────────────────────────────────────────────────"

  done
done

echo "Inference complete. Results in: ${INFERENCES_DIR}"
