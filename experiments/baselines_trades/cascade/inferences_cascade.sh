#!/bin/bash
#SBATCH --job-name=sst_cascade_grid
#SBATCH --partition=batch
#SBATCH --gpus=1
#SBATCH --cpus-per-task=4
#SBATCH --mem=32G
#SBATCH --output=/data/jagucam1@alumno.upv.es/MAIN/experiments/baselines_trades/cascade/slurm-%j.out

set -eo pipefail

# ── Environment setup ─────────────────────────────────────────────────────────
# Edit these variables with your paths before launching.
REPO_ROOT="/data/jagucam1@alumno.upv.es/MAIN"
CONDA_ENV="/data/jagucam1@alumno.upv.es/MAIN/env"
CONDA_ACTIVATE="/data/jagucam1@alumno.upv.es/miniconda3/bin/activate"

EXP_DIR="${REPO_ROOT}/experiments/baselines_trades/cascade"
CONFIG_DIR="${EXP_DIR}/configs"
INFERENCES_DIR="${EXP_DIR}/inferences"

# ── 2D grid ───────────────────────────────────────────────────────────────────
STEP_LABELS=("1.0" "2.0" "4.0" "8.0")
STEP_VALUES=("1.0" "2.0" "4.0" "8.0")
LA_LABELS=("1" "2" "5" "10")
LA_VALUES=("1" "2" "5" "10")

# ── Fixed parameters ──────────────────────────────────────────────────────────
SEGMENT_LENGTH="24"
TRANSCRIPT_CONTEXT="30"
MAX_INPUT_LENGTH_SOFT="30"
MAX_INPUT_LENGTH_HARD="60"
WHISPER_MODEL="small"
WHISPER_LANGUAGE="en"
TRANSLATION_MODEL="facebook/m2m100_418M"
TRANSLATION_LANGUAGE="de"

# ── Setup ─────────────────────────────────────────────────────────────────────
mkdir -p "${CONFIG_DIR}"
mkdir -p "${INFERENCES_DIR}"

cd "${REPO_ROOT}"

source "${CONDA_ACTIVATE}" "${CONDA_ENV}"

export PYTHONPATH="${REPO_ROOT}/src${PYTHONPATH:+:${PYTHONPATH}}"
export PATH="$HOME/.local/bin:$PATH"

# ── 2D grid loop ──────────────────────────────────────────────────────────────
for i in "${!STEP_LABELS[@]}"; do
  step_label="${STEP_LABELS[$i]}"
  step_val="${STEP_VALUES[$i]}"

  for j in "${!LA_LABELS[@]}"; do
    la_label="${LA_LABELS[$j]}"
    la_val="${LA_VALUES[$j]}"

    config_file="${CONFIG_DIR}/cascade_step${step_label}_la${la_label}.yaml"
    metrics_file="${INFERENCES_DIR}/cascade_step${step_label}_la${la_label}.jsonl"

    # Auto-generate config for this grid point
    cat > "${config_file}" <<CONFIG
# Auto-generated by inferences_cascade.sh
# step_length=${step_label}s | la_policy=${la_label}
type: "simulstream.server.speech_processors.simuleval_wrapper.SimulEvalWrapper"
simuleval_agent: "cascade.baseline_agent.CascadeAgentWitFixedLengthSegmenter"

latency_unit: "word"
speech_chunk_size: 0.5
detokenizer_type: "simuleval"

config:
  device: "cuda"
  segment_length: ${SEGMENT_LENGTH}.0
  step_length: ${step_val}
  whisper_model: "${WHISPER_MODEL}"
  whisper_language: "${WHISPER_LANGUAGE}"
  whisper_task: "transcribe"
  whisper_compute_type: "auto"
  translation_model: "${TRANSLATION_MODEL}"
  translation_language: "${TRANSLATION_LANGUAGE}"
  translation_la_policy: ${la_val}
  transcript_context: ${TRANSCRIPT_CONTEXT}
  translation_max_input_length_soft: ${MAX_INPUT_LENGTH_SOFT}
  translation_max_input_length_hard: ${MAX_INPUT_LENGTH_HARD}

agent_args:
  - "--segment-length"
  - "${SEGMENT_LENGTH}"
  - "--step-length"
  - "${step_val}"
  - "--whisper-model"
  - "${WHISPER_MODEL}"
  - "--whisper-language"
  - "${WHISPER_LANGUAGE}"
  - "--whisper-compute-type"
  - "auto"
  - "--translation-model"
  - "${TRANSLATION_MODEL}"
  - "--translation-language"
  - "${TRANSLATION_LANGUAGE}"
  - "--translation-la-policy"
  - "${la_val}"
  - "--transcript-context"
  - "${TRANSCRIPT_CONTEXT}"
  - "--translation-max-input-length-soft"
  - "${MAX_INPUT_LENGTH_SOFT}"
  - "--translation-max-input-length-hard"
  - "${MAX_INPUT_LENGTH_HARD}"
CONFIG

    # Skip if output already exists
    if [[ -f "${metrics_file}" ]]; then
      echo "[cascade] $(basename ${metrics_file}) already exists, skipping..."
      continue
    fi

    echo "════════════════════════════════════════════════════════════"
    echo "[cascade] step=${step_label}s | la_policy=${la_label}"
    echo "════════════════════════════════════════════════════════════"

    simulstream_inference \
      --speech-processor-config "${config_file}" \
      --wav-list-file "${REPO_ROOT}/data/dev/src.txt" \
      --tgt-lang deu \
      --src-lang eng \
      --metrics-log-file "${metrics_file}"

    echo "[cascade] Done step=${step_label}s | la_policy=${la_label}"
    echo "────────────────────────────────────────────────────────────"

  done
done

echo "Inference complete. Results in: ${INFERENCES_DIR}"
