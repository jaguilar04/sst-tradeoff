#!/bin/bash
#SBATCH --job-name=sst_fixed_grid
#SBATCH --partition=batch
#SBATCH --gpus=1
#SBATCH --cpus-per-task=4
#SBATCH --mem=32G
#SBATCH --output=/data/jagucam1@alumno.upv.es/MAIN/experiments/baselines_trades/fixed/slurm-%j.out

set -eo pipefail

# ── Environment setup ─────────────────────────────────────────────────────────
# Edit these variables with your paths before launching.
REPO_ROOT="/data/jagucam1@alumno.upv.es/MAIN"
CONDA_ENV="/data/jagucam1@alumno.upv.es/MAIN/env"
CONDA_ACTIVATE="/data/jagucam1@alumno.upv.es/miniconda3/bin/activate"

EXP_DIR="${REPO_ROOT}/experiments/baselines_trades/fixed"
CONFIG_DIR="${EXP_DIR}/configs"
INFERENCES_DIR="${EXP_DIR}/inferences"

# ── step_length sweep ─────────────────────────────────────────────────────────
STEP_LABELS=("0.5" "1.0" "2.0" "4.0" "offline")
STEP_VALUES=("0.5" "1.0" "2.0" "4.0" "8.0")

# ── Fixed parameters ──────────────────────────────────────────────────────────
SEGMENT_LENGTH="8.0"
SEAMLESS_MODEL="facebook/seamless-m4t-v2-large"
TGT_LANG="deu"

# ── Setup ─────────────────────────────────────────────────────────────────────
mkdir -p "${CONFIG_DIR}"
mkdir -p "${INFERENCES_DIR}"

cd "${REPO_ROOT}"

source "${CONDA_ACTIVATE}" "${CONDA_ENV}"

export PYTHONPATH="${REPO_ROOT}/src${PYTHONPATH:+:${PYTHONPATH}}"
export PATH="$HOME/.local/bin:$PATH"

# ── Parameter sweep loop ──────────────────────────────────────────────────────
for i in "${!STEP_LABELS[@]}"; do
  step_label="${STEP_LABELS[$i]}"
  step_val="${STEP_VALUES[$i]}"

  config_file="${CONFIG_DIR}/fixed_step${step_label}.yaml"
  metrics_file="${INFERENCES_DIR}/fixed_step${step_label}.jsonl"

  # Auto-generate YAML config
  cat > "${config_file}" <<CONFIG
# Auto-generated by inferences_fixed.sh
# step_length=${step_label}s | segment_length=${SEGMENT_LENGTH}s
type: "simulstream.server.speech_processors.simuleval_wrapper.SimulEvalWrapper"
simuleval_agent: "fixed_segmenter.baseline_fixed.SeamlessM4TAgentWithFixedSegmenter"

latency_unit: "word"
speech_chunk_size: 0.5
detokenizer_type: "simuleval"

config:
  device: "cuda"
  segment_length: ${SEGMENT_LENGTH}
  step_length: ${step_val}
  seamless_m4t_model: "${SEAMLESS_MODEL}"
  seamless_m4t_tgt_lang: "${TGT_LANG}"

agent_args:
  - "--segment-length"
  - "${SEGMENT_LENGTH}"
  - "--step-length"
  - "${step_val}"
  - "--seamless-m4t-model"
  - "${SEAMLESS_MODEL}"
  - "--seamless-m4t-tgt-lang"
  - "${TGT_LANG}"
  - "--device"
  - "cuda"
CONFIG

  # Skip if output already exists
  if [[ -f "${metrics_file}" ]]; then
    echo "[fixed] $(basename ${metrics_file}) already exists, skipping..."
    continue
  fi

  echo "════════════════════════════════════════════════════════════"
  echo "[fixed] segment=${SEGMENT_LENGTH}s | step=${step_label}s"
  echo "════════════════════════════════════════════════════════════"

  simulstream_inference \
    --speech-processor-config "${config_file}" \
    --wav-list-file "${REPO_ROOT}/data/dev/src.txt" \
    --tgt-lang deu \
    --src-lang eng \
    --metrics-log-file "${metrics_file}"

  echo "[fixed] Done step=${step_label}s"
  echo "────────────────────────────────────────────────────────────"

done

echo "Inference complete. Results in: ${INFERENCES_DIR}"
